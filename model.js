class model {

    constructor(radius, x, y){
        this.radius = radius;
        this.x = x;
        this.y = y;
    }

    getRadius(){
        return radius;
    }
    setRadius(radius){
        this.radius = radius;
    }

    getY(){
        return this.y;
    }
    setY(y){
        this.y = y;
    }

    getX(){
        return this.x;
    }
    setX(x){
        this.x = x;
    }
}
